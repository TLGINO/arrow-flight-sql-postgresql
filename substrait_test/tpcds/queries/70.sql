SELECT SUM(SUM("STORE_SALES3"."SS_NET_PROFIT")) AS "$f00", CAST(NULL AS CHAR(2)) AS "$f2", CAST(NULL AS VARCHAR(30)) AS "$f3", 1 AS "$f4", 1 AS "$f5", 2 AS "$f6"
FROM "tpcds"."STORE_SALES" AS "STORE_SALES3",
"tpcds"."DATE_DIM" AS "DATE_DIM3",
"tpcds"."STORE" AS "STORE3"
