FROM (SELECT "STORE4"."S_STATE" AS "S_STATE00", RANK() OVER (PARTITION BY "STORE4"."S_STATE" ORDER BY SUM("STORE_SALES4"."SS_NET_PROFIT") DESC) AS "$f3"
