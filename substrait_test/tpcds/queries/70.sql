SELECT "$f200" AS "TOTAL_SUM", "S_STATE000" AS "S_STATE", "S_COUNTY000" AS "S_COUNTY", "$f80" AS "LOCHIERARCHY", "$f10" AS "RANK_WITHIN_PARENT"
FROM (SELECT "$f20" AS "$f200", "S_STATE00" AS "S_STATE000", "S_COUNTY00" AS "S_COUNTY000", "$f8" AS "$f80", RANK() OVER (PARTITION BY "$f8", CASE WHEN "$f7" = 0 THEN "S_STATE00" ELSE NULL END ORDER BY "$f20" DESC) AS "$f10", CASE WHEN "$f8" = 0 THEN "S_STATE00" ELSE NULL END AS "$f11"
FROM (SELECT *
FROM (SELECT SUM("STORE_SALES"."SS_NET_PROFIT") AS "$f20", "STORE"."S_STATE" AS "S_STATE00", "STORE"."S_COUNTY" AS "S_COUNTY00", 0 AS "$f6", 0 AS "$f7", 0 AS "$f8"
FROM "tpcds"."STORE_SALES",
"tpcds"."DATE_DIM",
"tpcds"."STORE"
WHERE "DATE_DIM"."D_MONTH_SEQ" >= 1220 AND "DATE_DIM"."D_MONTH_SEQ" <= 1220 + 11 AND "DATE_DIM"."D_DATE_SK" = "STORE_SALES"."SS_SOLD_DATE_SK" AND "STORE"."S_STORE_SK" = "STORE_SALES"."SS_STORE_SK" AND "STORE"."S_STATE" IN (SELECT "S_STATE00" AS "S_STATE000"
FROM (SELECT "STORE0"."S_STATE" AS "S_STATE00", RANK() OVER (PARTITION BY "STORE0"."S_STATE" ORDER BY SUM("STORE_SALES0"."SS_NET_PROFIT") DESC) AS "$f3"
FROM "tpcds"."STORE_SALES" AS "STORE_SALES0",
"tpcds"."STORE" AS "STORE0",
"tpcds"."DATE_DIM" AS "DATE_DIM0"
WHERE "DATE_DIM0"."D_MONTH_SEQ" >= 1220 AND "DATE_DIM0"."D_MONTH_SEQ" <= 1220 + 11 AND "DATE_DIM0"."D_DATE_SK" = "STORE_SALES0"."SS_SOLD_DATE_SK" AND "STORE0"."S_STORE_SK" = "STORE_SALES0"."SS_STORE_SK"
GROUP BY "STORE0"."S_STATE") AS "t2"
WHERE "t2"."$f3" <= 5)
GROUP BY "STORE"."S_STATE", "STORE"."S_COUNTY"
UNION
