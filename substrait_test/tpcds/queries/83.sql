(SELECT "ITEM1"."I_ITEM_ID" AS "I_ITEM_ID0", SUM("WEB_RETURNS"."WR_RETURN_QUANTITY") AS "$f1"
FROM "tpcds"."WEB_RETURNS",
"tpcds"."ITEM" AS "ITEM1",
"tpcds"."DATE_DIM" AS "DATE_DIM5"
WHERE "WEB_RETURNS"."WR_ITEM_SK" = "ITEM1"."I_ITEM_SK" AND "DATE_DIM5"."D_DATE" IN (SELECT "D_DATE" AS "D_DATE0"
