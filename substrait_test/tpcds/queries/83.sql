FROM (SELECT "ITEM"."I_ITEM_ID" AS "I_ITEM_ID0", SUM("STORE_RETURNS"."SR_RETURN_QUANTITY") AS "$f1"
FROM "tpcds"."STORE_RETURNS",
"tpcds"."ITEM",
"tpcds"."DATE_DIM"
