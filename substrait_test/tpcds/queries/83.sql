GROUP BY "ITEM"."I_ITEM_ID") AS "t5",
(SELECT "ITEM0"."I_ITEM_ID" AS "I_ITEM_ID0", SUM("CATALOG_RETURNS"."CR_RETURN_QUANTITY") AS "$f1"
FROM "tpcds"."CATALOG_RETURNS",
"tpcds"."ITEM" AS "ITEM0",
"tpcds"."DATE_DIM" AS "DATE_DIM2"
WHERE "CATALOG_RETURNS"."CR_ITEM_SK" = "ITEM0"."I_ITEM_SK" AND "DATE_DIM2"."D_DATE" IN (SELECT "D_DATE" AS "D_DATE0"
FROM "tpcds"."DATE_DIM"
